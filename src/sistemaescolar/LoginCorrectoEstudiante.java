    private void contarClave() {
        labelClave.setText("");
        String valorAnterior = labelClave.getText();
        for (int i = 1; i < password.length(); i++) {
            labelClave.setText(valorAnterior + "•");
            valorAnterior = labelClave.getText();
            labelClave.setText(valorAnterior + "•");
        }
        claveFinal = labelClave.getText();
    }

